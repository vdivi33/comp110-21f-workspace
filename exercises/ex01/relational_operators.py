"""Relational operator demonstration!"""
__author__ = "730470448"

left_hand: str = input("Left-hand side: ")
right_hand: str = input("Right-hand side: ")
print(left_hand + " < " + right_hand + " is " + str(left_hand < right_hand))
print(left_hand + " >= " + right_hand + " is " + str(left_hand >= right_hand))
print(left_hand + " == " + right_hand + " is " + str(left_hand == right_hand))
print(left_hand + " != " + right_hand + " is " + str(left_hand != right_hand))
